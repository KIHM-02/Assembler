.DEFINE
tabla1 0000H;

.DATA 0000H
db 10H, 20H, 05H, 30H;

.ORG 1000H

LXI H, tabla1;

CONTAR:

	MOV A, M;
	INR A;
	DCR A;
		JZ ORDENAR;
	INR B;
	INX H;
	JMP CONTAR;


ORDENAR:

LXI H, tabla1; 

CONTINUAR_ORDENANDO:

	MOV A, M;
	INR A;
	DCR A;
		JZ FOR;

	INX H;
	CMP M;
		JNC A_MAYOR_QUE;

	MOV D, M;
	MOV M, A;
	DCX H;
	MOV M, D;
	INX H;
		JMP CONTINUAR_ORDENANDO;


A_MAYOR_QUE:
	JMP CONTINUAR_ORDENANDO;

FOR:
	DCR B;
		JNZ ORDENAR;	


HLT